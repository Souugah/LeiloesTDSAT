    private void preencherTabela() {
                ProdutosDAO produtosDAO = new ProdutosDAO();
            
                List<ProdutosDTO> listaProdutosVendidos = produtosDAO.getProdutosVendidos();
            
                DefaultTableModel tabelaVendas = (DefaultTableModel) tblVendas.getModel();
            
                tblVendas.setRowSorter(new TableRowSorter(tabelaVendas));
                      
                for (ProdutosDTO p : listaProdutosVendidos) { //em cada volta do la√ßo for, o mesmo adiciona uma dado(empresa) dentro do objeto c
                    Object[] obj = new Object[] { 
                        p.getId(),     //id
                        p.getNome(),   //nome
                        p.getValor(),  //valor
                        p.getStatus(), //status
                        
                    };
                    tabelaVendas.addRow(obj);
                }    
            }

    
